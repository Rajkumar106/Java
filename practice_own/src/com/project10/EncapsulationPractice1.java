package com.project10;

public class EncapsulationPractice1 { //POJO class
	private String name;
	private int age;
	
	public void setName(String s) {
		this.name =s;
	}
	public String getName() {
		return this.name;
	}
	public void setAge(int a) {
		this.age=a;
	}
	public int getAge() {
		return this.age;
	}
	
}//


